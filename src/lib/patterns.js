import Tcomb from "tcomb-form-native";


let Email = Tcomb.subtype(Tcomb.Str, (s) => {
  // let re = new RegExp(["^(([^<>()[\]\\.,;:\\s@\"]+(\\.[^<>(),[\]\\.,;:\\s@\"]+)*)",
  //                       "|(\\'.+\\'))@((\\[[0-9]{1,3}\\.[0-9]{1,3}\\.[0-9]{1,3}\\.",
  //                       "[0-9]{1,3}\])|(([a-zA-Z\-0-9]+\\.)+",
  //                       "[a-zA-Z]{2,}))$"].jo in(""));
  let re = /^(([^<>()\[\]\\.,;:\s@"]+(\.[^<>()\[\]\\.,;:\s@"]+)*)|(".+"))@((\[[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}])|(([a-zA-Z\-0-9]+\.)+[a-zA-Z]{2,}))$/;
  return re.test(s);
});

export {
  Email,
};